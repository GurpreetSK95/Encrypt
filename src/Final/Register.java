    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            //Get connection with the database
            DriverManager.registerDriver(new com.mysql.jdbc.Driver());
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cryptofer?user=root&password=");
            PreparedStatement ps = con.prepareStatement("insert into crypt(name, email, password) values (?,?,?)");
            ps.setString(1, jTextField1.getText());
            ps.setString(2, jTextField2.getText());
            ps.setString(3, jTextField3.getText());
            int i = ps.executeUpdate();             //tells whether insertion was successful or not
            System.out.println("Inserted: " + i);
            if (i == 1) 
                JOptionPane.showMessageDialog(null, "You have registered successfully.", "Congratulations!", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(null, "lease try again.", "ERROR", JOptionPane.ERROR_MESSAGE);
            new Working().setVisible(true);
        } catch (Exception e) {
            System.out.println(e);
        }
        this.setVisible(false);
    }//GEN-LAST:event_jButton1ActionPerformed

